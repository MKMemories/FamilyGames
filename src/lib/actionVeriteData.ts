/** Tire une invite non répétée (dans la partie + parties récentes). */
export function pickPrompt(
  type: "verite" | "action",
  used: number[],
  recent: Set<string>,
  rnd: () => number = Math.random,
): { idx: number; text: string } {
  const bank = type === "verite" ? VERITES : ACTIONS;
  const p = type === "verite" ? "v" : "a";
  let pool = bank.map((t, i) => ({ t, i })).filter(x => !used.includes(x.i) && !recent.has(`${p}:${x.i}`));
  if (pool.length === 0) pool = bank.map((t, i) => ({ t, i })).filter(x => !used.includes(x.i));
  if (pool.length === 0) pool = bank.map((t, i) => ({ t, i }));
  const c = pool[Math.floor(rnd() * pool.length)];
  return { idx: c.i, text: c.t };
}
